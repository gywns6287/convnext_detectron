@BACKBONE_REGISTRY.register()
def build_convnext_fpn_backbone(cfg, input_shape: ShapeSpec):
    """
    Args:
        cfg: a detectron2 CfgNode

    Returns:
        backbone (Backbone): backbone module, must be a subclass of :class:`Backbone`.
    """
    TYPE = cfg.MODEL.CONVNEXT.TYPE.lower()
    if TYPE == 't':
        bottom_up = convnext_tiny(pretrained=True,in_22k=True, out_features = cfg.MODEL.CONVNEXT.OUT_FEATURES)
    elif TYPE == 's':
        bottom_up = convnext_small(pretrained=True,in_22k=True, out_features = cfg.MODEL.CONVNEXT.OUT_FEATURES)
    elif TYPE == 'b':
        bottom_up = convnext_base(pretrained=True,in_22k=True, out_features = cfg.MODEL.CONVNEXT.OUT_FEATURES)
    elif TYPE == 'l':
        bottom_up = convnext_large(pretrained=True,in_22k=True, out_features = cfg.MODEL.CONVNEXT.OUT_FEATURES)
    elif TYPE == 'xl':
        bottom_up = convnext_xlarge(pretrained=True,in_22k=True, out_features = cfg.MODEL.CONVNEXT.OUT_FEATURES)

    in_features = cfg.MODEL.FPN.IN_FEATURES
    out_channels = cfg.MODEL.FPN.OUT_CHANNELS
    backbone = FPN(
        bottom_up=bottom_up,
        in_features=in_features,
        out_channels=out_channels,
        norm=cfg.MODEL.FPN.NORM,
        top_block=LastLevelMaxPool(),
        fuse_type=cfg.MODEL.FPN.FUSE_TYPE,
    )

    return backbone



